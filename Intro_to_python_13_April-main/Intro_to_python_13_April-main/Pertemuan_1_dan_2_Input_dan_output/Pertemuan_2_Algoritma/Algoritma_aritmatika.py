x = 5
y = 2

# penjumlahan
hasil = x + y 
print(f"Hasil dari Penjumlahan {x} + {y} = {hasil}")
# kurang
hasil = x - y 
print(f"Hasil dari kurang {x} - {y} = {hasil}")
# kali
hasil = x * y 
print(f"Hasil dari kali {x} * {y} = {hasil}")
# bagi
hasil = x / y 
# conversi
hasil_conversi = int(hasil)
# cara cepet
hasil_cepat = x // y 
print(f"Hasil dari bagi {x} / {y} = {hasil}")
print(f"hasil dari bagi conversi int {x} / {y} = {hasil_conversi}")
print(f"hasil dari bagi conversi int cepat {x} // {y} = {hasil_conversi}")
# modulus (%)
# modulus = sisa dari pembagian 
hasil = x % y
print(f"hasil dari modulus  {x} % {y} = {hasil}")
# pangkat 
hasil = x ** y 
print(f"hasil dari pangkat  {x} ^ {y} = {hasil}")
# Cara simpel 
x = x + 1
print(f"Hasil x = {x}")
# incerment
x += 1
print(f"hasil x = {x}")
# decerment
x -= 1
print(f"hasil x = {x}")